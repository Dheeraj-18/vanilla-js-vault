// Node (Generic)
// a) A Node is the basic building block of the DOM.

// b) Everything in the DOM (elements, text, comments, etc.) is a node.

// c) It is a generic interface from which other types inherit.



// Element (Specific Type of Node)
// 1) An Element is a special kind of node.

// 2) Specifically represents HTML elements like <div>, <p>, <img>, etc.

// 3) Elements can have attributes and children (text or other elements).

//  Quick Summary:
// All elements are nodes, but not all nodes are elements.

//  NOTE: every type of object in DOM is Node   `